variavel = input('Digite algo: ')

print('O tipo de valor digitado é do tipo: ', type(variavel))
print('Só tem espaço: ', variavel.isspace())
print('É numerico: ', variavel.isnumeric())
print('É alfabetico: ', variavel.isalpha())
print('É alfanumerico: ', variavel.isalnum())
print('É somente maiuscula: ', variavel.isupper())
print('É somente minusculas: ', variavel.islower())
print('É capitalizada: ', variavel.istitle())
print('É decimal: ', variavel.isdecimal())

